export default pathCards

export function getCardsForUser(interests: PathCategory[], shown: string[], horizon?: Horizon): PathCard[] {
  let filtered = pathCards.filter(c => !shown.includes(c.id))
  if (interests.length > 0) {
    const prioritized = filtered.filter(c => interests.includes(c.category))
    if (prioritized.length > 0) filtered = prioritized
  }
  if (horizon) {
    const h = filtered.filter(c => c.horizon === horizon)
    if (h.length > 0) filtered = h
  }
  return filtered.slice(0, 3)
}
